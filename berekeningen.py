def plus(a, b):
    return(a+b)

def min(a,b):
    return(a-b)

def keer(a,b):
    return(a*b)

def delen(a,b):
    return(a/b)

def increment(a):
    return(a+1)

def decrement(a):
    return(a-1)

print('10', '+', '12', ' = ', plus(10,12))
print('58', '-', '34', ' = ', min(58,34))
print('6', 'x', '7', ' = ', keer(6,7))
print('144', ':', '12', ' = ', int(delen(144,12)))
print('12', '+', '1', ' = ', increment(12))
print('34', '-', '1', ' = ', decrement(34))
